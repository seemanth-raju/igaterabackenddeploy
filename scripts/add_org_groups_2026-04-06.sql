ALTER TABLE public.tenant_group
    ADD COLUMN IF NOT EXISTS short_name character varying(50);

ALTER TABLE public.tenant_group
    ADD COLUMN IF NOT EXISTS description text;

ALTER TABLE public.tenant_group
    ADD COLUMN IF NOT EXISTS is_default boolean DEFAULT false NOT NULL;

ALTER TABLE public.tenant_group
    ADD COLUMN IF NOT EXISTS is_active boolean DEFAULT true NOT NULL;

ALTER TABLE public.tenant_group
    ADD COLUMN IF NOT EXISTS created_at timestamp with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL;

ALTER TABLE public.tenant_group
    ADD COLUMN IF NOT EXISTS updated_at timestamp with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL;

ALTER TABLE public.tenant_group
    DROP CONSTRAINT IF EXISTS uq_tenant_group_group_type_code;

ALTER TABLE public.tenant_group
    DROP CONSTRAINT IF EXISTS uq_tenant_group_group_type_name;

