    ADD COLUMN IF NOT EXISTS company_id uuid REFERENCES public.company(company_id) ON DELETE CASCADE;

ALTER TABLE public.tenant_group
    ADD COLUMN IF NOT EXISTS parent_group_id integer REFERENCES public.tenant_group(group_id) ON DELETE SET NULL;

ALTER TABLE public.tenant_group
