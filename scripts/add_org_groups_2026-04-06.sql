DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'uq_tenant_group_company_code'
    ) THEN
        ALTER TABLE public.tenant_group
            ADD CONSTRAINT uq_tenant_group_company_code UNIQUE (company_id, code);
    END IF;
