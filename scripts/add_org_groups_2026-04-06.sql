BEGIN;

CREATE TABLE IF NOT EXISTS public.tenant_group (
    group_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id uuid NOT NULL REFERENCES public.company(company_id) ON DELETE CASCADE,
    parent_group_id integer REFERENCES public.tenant_group(group_id) ON DELETE SET NULL,
    name character varying(100) NOT NULL,
    code character varying(50) NOT NULL,
    email character varying(255),
    short_name character varying(50),
    description text,
    is_default boolean DEFAULT false NOT NULL,
    is_active boolean DEFAULT true NOT NULL,
    created_at timestamp with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at timestamp with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT uq_tenant_group_company_code UNIQUE (company_id, code),
    CONSTRAINT uq_tenant_group_company_name UNIQUE (company_id, name)
);

